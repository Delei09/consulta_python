
class Calculadora :
    def somar ( num1 , num2 ) :
        print(num1 + num2)
    def subtrair ( num1 , num2 ) :
         print(num1 - num2)
    def multiplicar ( num1 , num2 ) :
         print(num1 * num2)
    def dividir ( num1 , num2 ) :
        print(num1 / num2)

calculadora = Calculadora
calculadora.somar(10 , 10 )
calculadora.subtrair(1212 , 1233213)
calculadora.dividir(2132 , 31)
calculadora.multiplicar(12 , 3)